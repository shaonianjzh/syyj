package syyj.domain;

public class Join {
    private String joinAdvantage;
    private String joinCondition;
    private String joinProgress;

    public String getJoinAdvantage() {
        return joinAdvantage;
    }

    public void setJoinAdvantage(String joinAdvantage) {
        this.joinAdvantage = joinAdvantage;
    }

    public String getJoinCondition() {
        return joinCondition;
    }

    public void setJoinCondition(String joinCondition) {
        this.joinCondition = joinCondition;
    }

    public String getJoinProgress() {
        return joinProgress;
    }

    public void setJoinProgress(String joinProgress) {
        this.joinProgress = joinProgress;
    }
}
